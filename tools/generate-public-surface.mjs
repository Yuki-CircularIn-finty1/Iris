import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const toolsDir = path.dirname(fileURLToPath(import.meta.url));
const root = path.dirname(toolsDir);
const manifest = JSON.parse(await readFile(path.join(toolsDir, 'public-pages.json'), 'utf8'));
const pages = manifest.pages;
const privateSlugs = manifest.privateSlugs;
const generatedDir = path.join(toolsDir, 'generated');

function normalizeSlash(value) {
  if (!value || !String(value).trim()) return '/';
  return `/${String(value).replace(/^\/+|\/+$/g, '')}`;
}

function escapeHtml(value) {
  return String(value)
    .replaceAll('&', '&amp;')
    .replaceAll('"', '&quot;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;');
}

for (const page of pages) {
  if (privateSlugs.includes(page.slug)) {
    throw new Error(`Public page slug '${page.slug}' is also listed as private.`);
  }
  if (privateSlugs.map((slug) => `${slug}.html`).includes(page.html)) {
    throw new Error(`Public page html '${page.html}' is also listed as private.`);
  }
  try {
    await readFile(path.join(root, page.html));
  } catch {
    throw new Error(`Manifest page '${page.slug}' points to missing file '${page.html}'.`);
  }
}

await mkdir(generatedDir, { recursive: true });

const redirectLines = [
  '# Generated by tools/generate-public-surface.mjs. Edit tools/public-pages.json instead.',
  '# Redirect legacy and inconsistent URL variants to canonical public routes.',
];
for (const redirect of manifest.legacyRedirects) {
  if (redirect.from && redirect.to) redirectLines.push(`${redirect.from} ${redirect.to} 301`);
}
redirectLines.push('');
for (const page of pages) {
  if (page.redirectHtml === false) continue;
  redirectLines.push(`/${page.html} ${normalizeSlash(page.slug)} 301`);
}
redirectLines.push('/_private/* /404.html 404');
redirectLines.push('/tools/* /404.html 404');
redirectLines.push('/.github/* /404.html 404');
await writeFile(path.join(root, '_redirects'), `${redirectLines.join('\r\n')}\r\n`);

const sitemapLines = [
  '<?xml version="1.0" encoding="utf-8"?>',
  '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">',
];
for (const page of pages) {
  const route = normalizeSlash(page.slug);
  const loc = route === '/' ? `${manifest.baseUrl}/` : `${manifest.baseUrl}${route}`;
  sitemapLines.push('  <url>');
  sitemapLines.push(`    <loc>${loc}</loc>`);
  sitemapLines.push(`    <lastmod>${page.lastmod || manifest.defaultLastmod}</lastmod>`);
  sitemapLines.push('  </url>');
}
sitemapLines.push('</urlset>');
await writeFile(path.join(root, 'sitemap.xml'), `${sitemapLines.join('\r\n')}\r\n`);

const listEntries = [];
for (const page of pages) {
  if (Object.hasOwn(page, 'listSlug') && page.listSlug === null) continue;
  const entry = Object.hasOwn(page, 'listSlug') ? page.listSlug : page.slug;
  if (entry) listEntries.push(entry);
}
listEntries.push(...manifest.staticFiles);
for (const page of pages) {
  if (page.html !== 'index.html') listEntries.push(page.html);
}
await writeFile(path.join(root, 'list.txt'), `${listEntries.join('\r\n')}\r\n`);

const robotsLines = [
  '# Generated by tools/generate-public-surface.mjs. Edit tools/public-pages.json instead.',
  'User-agent: *',
  'Disallow: /_private/',
  'Disallow: /tools/',
  'Disallow: /.github/',
  ...privateSlugs.map((slug) => `Disallow: /${slug}`),
  '',
  `Sitemap: ${manifest.baseUrl}/sitemap.xml`,
];
await writeFile(path.join(root, 'robots.txt'), `${robotsLines.join('\r\n')}\r\n`);

const navigation = pages
  .filter((page) => page.slug)
  .map((page) => ({ title: page.title, path: normalizeSlash(page.slug), html: page.html }));
await writeFile(
  path.join(generatedDir, 'public-navigation.json'),
  `${JSON.stringify(navigation, null, 2)}\n`,
);

const navHtmlLines = [
  '<!-- Generated by tools/generate-public-surface.mjs. Edit tools/public-pages.json instead. -->',
  '<nav aria-label="Public pages">',
  '  <ul>',
  ...navigation.map((item) => `    <li><a href="${escapeHtml(item.path)}">${escapeHtml(item.title)}</a></li>`),
  '  </ul>',
  '</nav>',
];
await writeFile(path.join(generatedDir, 'public-navigation.inc'), `${navHtmlLines.join('\r\n')}\r\n`);

console.log('Generated _redirects, sitemap.xml, list.txt, robots.txt, and tools/generated navigation files.');
